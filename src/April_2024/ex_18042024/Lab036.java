package April_2024.ex_18042024;

public class Lab036 {
    public static void main(String[] args) {
        // Increment & Decrement Operator
        // Two types:- Pre and Post
        int a = 10;
        int pre = ++a;
        System.out.println(pre); // Pre increment value will be printed first and then stored in variable...
        System.out.println(a); // After pre incremented value...
    }
}
